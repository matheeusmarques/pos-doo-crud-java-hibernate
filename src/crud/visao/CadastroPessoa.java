    private void preencherComboCidades() {
        CidadeController cidadeController = new CidadeController();;
        List<Cidade> cidades = new ArrayList<Cidade>();
        cidades = cidadeController.atualizarListaCidades();

        for (Cidade cidade : cidades) {
            System.out.println("Nome" + cidade.getNome());
            idCidades.add(cidade);
            comboCidades.addItem(cidade.getNome());
        }
    }

    private void atualizarListaPessoas() {
        PessoaController pessoaController = new PessoaController();
        List<Pessoa> pessoas = new ArrayList<>();

        pessoas = pessoaController.buscarTodos();

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        for (Pessoa pessoa : pessoas) {
            Object[] row = {pessoa.getId(), pessoa.getNome(), pessoa.getRg(), pessoa.getCpf(), pessoa.getIdcidade()};
            model.addRow(row);
        }

    }

