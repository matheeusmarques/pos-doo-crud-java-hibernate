    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        jButton1.setEnabled(false);
        jButton2.setEnabled(true);
        jButton3.setEnabled(true);
        p.setId(Integer.parseInt(jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString()));

        campoCpf.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 3).toString());
        campoNome.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 1).toString());
        campoRg.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 2).toString());

        CidadeController cidadeController = new CidadeController();
        Cidade cidade = new Cidade();
        cidade.setId(Integer.parseInt(jTable1.getValueAt(jTable1.getSelectedRow(), 4).toString()));
        cidade = cidadeController.buscar(cidade);
        int size = comboCidades.getItemCount();

        for (int i = 0; i < size; i++) {
            String item = comboCidades.getItemAt(i);
            if (item.equals(cidade.getNome())) {
                System.out.println("Item at " + i + " = " + item);
                comboCidades.setSelectedIndex(i);
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

